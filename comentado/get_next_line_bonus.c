#include "get_next_line_bonus.h"
#include <unistd.h>

char	*ft_read_to_left_str(int fd, char *left_str)
{
	char	*buff;
	int		rd_bytes;

	buff = malloc((BUFFER_SIZE + 1) * sizeof(char));
	if (!buff)
		return (NULL);
	rd_bytes = 1;
	while (!ft_strchr(left_str, '\n') && rd_bytes != 0)
	{
		rd_bytes = read(fd, buff, BUFFER_SIZE);
		if (rd_bytes == -1)
		{
			free(buff);
			return (NULL);
		}
		buff[rd_bytes] = '\0';
		left_str = ft_strjoin(left_str, buff);
	}
	free(buff);
	return (left_str);
}

char	*get_next_line(int fd)
{
	char		*line;
	// Declare static array to store the leftover strings for each file descriptor
	static char	*left_str[4096];
	// Check for invalid file descriptor or buffer size
	if (fd < 0 || BUFFER_SIZE <= 0)
		return (0);
	// Read from the file descriptor and store the leftover string in the array
	left_str[fd] = ft_read_to_left_str(fd, left_str[fd]);
	// If the leftover string is null, return null
	if (!left_str[fd]) 
		return (NULL);
	// Get the next line from the leftover string
	line = ft_get_line(left_str[fd]);
	// Update the leftover string for the file descriptor
	left_str[fd] = ft_new_left_str(left_str[fd]);
	// Return the line read from the file descriptor
	return (line);
}


